public class Inhalt
{
    private String land;
    private String co2;

    public Inhalt(String land, String co2)
    {
        this.land = land;
        this.co2 = co2;
    }

    public String getLand()
    {
        return land;
    }

    public String getCo2()
    {
        return co2;
    }
    
    
}
